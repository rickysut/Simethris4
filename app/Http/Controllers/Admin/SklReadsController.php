	{
		$sklId = $request->input('skl_id');
		$userId = Auth::id();

		// Simpan data pembacaan SKL ke dalam tabel skl_reads
		SklReads::updateOrCreate(
			['skl_id' => $sklId, 'user_id' => $userId],
			['read_at' => now()]
		);

		return response()->json(['message' => 'SKL sudah ditandai sebagai sudah dibaca']);
	}

